// Sort keys longest-first to ensure proper matching
const keys = Object.keys(RUBY_DICTIONARY).sort((a, b) => b.length - a.length);
const REGEX_PATTERN = new RegExp(`(${keys.join('|')})`, 'g');

interface RubyTextProps {
  text: string;
  enabled?: boolean;
}

export default function RubyText({ text, enabled = false }: RubyTextProps) {
  if (!enabled || !text) {
    return <>{text}</>;
  }

  const parts = text.split(REGEX_PATTERN);

  return (
    <>
      {parts.map((part, index) => {
        const furigana = RUBY_DICTIONARY[part];
        if (furigana) {
          return (
            <ruby key={index} className="ruby-text">
              {part}
              <rt className="text-[10px] text-emerald-600 font-normal leading-normal tracking-wide">
                {furigana}
              </rt>
            </ruby>
          );
        }
        return <React.Fragment key={index}>{part}</React.Fragment>;
      })}
    </>
  );
}
